//Circulo
let PerimetroCirculo=document.getElementById("PerimetroCirculo");
let ventanaPerimetroCirculo=document.getElementById("ventanaPerimetroCirculo");
let cerrarPerimetroCirculo=document.querySelector(".cerrarPerimetroCirculo");
PerimetroCirculo.addEventListener("click",()=>{
    ventanaPerimetroCirculo.style.display="block";
})
cerrarPerimetroCirculo.addEventListener("click",()=>{
    ventanaPerimetroCirculo.style.display="none"; 
})
window.addEventListener("click",(e)=>{
if(e.target==ventanaPerimetroCirculo){
    ventanaPerimetroCirculo.style.display="none"; 
}
})

